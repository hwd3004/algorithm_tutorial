# 3. 1부터 n까지 연속한 숫자의 제곱의 합을 구하는 공식
# 예를 들어 n = 10이라면 1**2 + 2**2 +3**2 + … + 10**2 = 385를
# 이를 계산하는 공식은 다음과 같다.
# s = (n * (n + 1) * (2 * n + 1)) / 6
# 이를 계산하는 프로그램은 다음과 같다.
# 이 공식을 이용하면 알고리즘의 계산 복잡도는 O(1)이다.


def main2(n: int):
    s = (n * (n + 1) * (2 * n + 1)) // 6
    print(s)
    return s


main2(10)
