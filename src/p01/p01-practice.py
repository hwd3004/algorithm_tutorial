# 1장 연습 문제

# 1.
# 1부터 n까지 연속한 숫자의 제곱의 합을 구하는 프로그램을 for 반복문
# 예를 들어 n = 10이라면 12 + 22 +32 + … + 102 = 385를 계산하는 프로그램

def main(n: int):
    s = 0
    for i in range(1, n + 1):
        s = s + i ** 2
        print(s)
    return s


main(10)

